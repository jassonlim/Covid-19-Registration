    public PersonnelVaccinationAppointmentFrame(String personnelEmail, String personnelPassword) {
        initComponents();
        this.personnelEmail = personnelEmail;
        this.personnelPassword = personnelPassword;
        DefaultTableModel tblModel = (DefaultTableModel)jTable2.getModel();
        try {
            BufferedReader reader = new BufferedReader(new FileReader("Appointment.txt"));
            String line;
            while ((line = reader.readLine()) != null) {
                String [] row = line.split("\t");
                String data1[] = {row[0], row[1], row[2], row[3], row[4], row[5]};
                tblModel.addRow(data1);
            }
        } catch (FileNotFoundException e) {
        } catch (IOException e) {
        }

        Vaccine vaccine = new Vaccine();
        // get all available vaccines
        ArrayList<String[]> vaccines = vaccine.getAvailableVaccines();
        // add the data to combo box
        for (String[] vaccineDetails : vaccines) {
            vaccineComboBox.addItem(vaccineDetails[0]);
        }
        
        Centre centre = new Centre();
        // get all available Vaccination Centres
        ArrayList<String[]> centres = centre.getAvailableCentres();
        // get the table model which displays all the data
        DefaultTableModel tableModel = (DefaultTableModel) centresTable1.getModel();
        
        // add the data to the row in table
        for (String[] centreDetails : centres) {
            tableModel.addRow(centreDetails);
        }
    }

