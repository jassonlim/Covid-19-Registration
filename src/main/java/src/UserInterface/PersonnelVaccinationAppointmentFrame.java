    private void displayDetails(String email) {
        try(BufferedReader file = new BufferedReader(new FileReader("Appointment.txt"))) {
            String line;    // to store each line
            while((line = file.readLine()) != null) {
                String[] details = line.split("\t");    // convert string to array of strings
                if (details[1].equals(email.toLowerCase())) {
                    
                    codeInput.setText(details[0]);
                    emailInput.setText(details[1]);
                    vaccineTypeInput.setText(details[2]);
                    dateTimeInput.setText(details[3]);
                    centreNameInput.setText(details[4]);
                    vaccineDoseInput.setText(details[5]);
                }
            }
        } catch (Exception e) {
            System.out.println("details are not found");
        }
    }

